B1RunAction::B1RunAction()
: G4UserRunAction()
{ 
  G4cout << "hello" << G4endl;
  // set printing event number per each event
  G4RunManager::GetRunManager()->SetPrintProgress(1);     

  // Create analysis manager
  // The choice of analysis technology is done via selectin of a namespace
  // in B4Analysis.hh
  auto analysisManager = G4AnalysisManager::Instance();
  G4cout << "Using " << analysisManager->GetType() << G4endl;

  // Create directories 
  //analysisManager->SetHistoDirectoryName("histograms");
  //analysisManager->SetNtupleDirectoryName("ntuple");
  analysisManager->SetVerboseLevel(1);
  analysisManager->SetNtupleMerging(true);
    // Note: merging ntuples is available only with Root output

  // Book histograms, ntuple
  //
  
  // Creating histograms
  analysisManager->CreateH1("Eabs","Edep in absorber", 100, 0., 800*MeV);
  
  // Creating ntuple
  // 默认 ntupleId 从0开始加，如果只维护一个ntuple，可以省略ID
  analysisManager->CreateNtuple("myB1", "Edep"); // name, title
  analysisManager->CreateNtupleIColumn("Event_ID"); //I型，先尝试全局维护一个Ntuple吧
  analysisManager->CreateNtupleIColumn("cell_ID"); //I型，XColumn = I,F,D,S // ntupleID = 0，很神奇。。这里如果指定ntupleID似乎不行
  analysisManager->CreateNtupleIColumn("x_index"); // 将cellID拆成xyz坐标
  analysisManager->CreateNtupleIColumn("y_index");
  // analysisManager->CreateNtupleIColumn("z_index"); // 被折叠了
  analysisManager->CreateNtupleDColumn("Edep"); //浮点型

  analysisManager->FinishNtuple(); // 创建好所有的Ntuple Colum后调用Finish

}

//....oooOO0OOooo........oooOO0OOooo........oooOO0OOooo........oooOO0OOooo......

B1RunAction::~B1RunAction()
{
  delete G4AnalysisManager::Instance();  
}

//....oooOO0OOooo........oooOO0OOooo........oooOO0OOooo........oooOO0OOooo......

void B1RunAction::BeginOfRunAction(const G4Run* run)
{ 
  //inform the runManager to save random number seed
  //G4RunManager::GetRunManager()->SetRandomNumberStore(true);
  
  // Get analysis manager
  auto analysisManager = G4AnalysisManager::Instance();

  // Open an output file
  //
  G4int runID = run->GetRunID();
  
  G4String fileName = "myB1_"+ std::to_string(runID);
  analysisManager->OpenFile(fileName);

}

//....oooOO0OOooo........oooOO0OOooo........oooOO0OOooo........oooOO0OOooo......
void B1RunAction::EndOfRunAction(const G4Run* /*run*/)
{
  // print histogram statistics
  //
  auto analysisManager = G4AnalysisManager::Instance();
  if ( analysisManager->GetH1(1) ) {
    G4cout << G4endl << " ----> print histograms statistic ";
    if(isMaster) {
      G4cout << "for the entire run " << G4endl << G4endl; 
    }
    else {
      G4cout << "for the local thread " << G4endl << G4endl; 
    }
    
    G4cout << " EAbs : mean = " 
       << G4BestUnit(analysisManager->GetH1(0)->mean(), "Energy") 
       << " rms = " 
       << G4BestUnit(analysisManager->GetH1(0)->rms(),  "Energy") << G4endl;
      }


  // save histograms & ntuple
  //
  analysisManager->Write();
  analysisManager->CloseFile();
}
